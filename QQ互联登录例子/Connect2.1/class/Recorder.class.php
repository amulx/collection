require_once(QQ_CONNEC_SDK_CLASS_PATH."ErrorCase.class.php");
class Recorder{
    private static $data;
    private $inc;
    private $error;

    public function __construct(){
        $this->error = new ErrorCase();

        //-------¶ÁÈ¡ÅäÖÃ
        $this->inc->appid = '101286303';
        $this->inc->appkey = 'c3494ed6564c3efbdda6ec7a3d9e5268';
        $this->inc->callback = 'http://www.333f.com/amu.php';
        $this->inc->scope = 'get_user_info';
        $this->inc->errorReport = 'true';
        $this->inc->storageType = 'file';

        if(empty($this->inc)){
            $this->error->showError("20001");
        }

        if(empty($_SESSION['QC_userData'])){
            self::$data = array();
        }else{
            self::$data = $_SESSION['QC_userData'];
        }
    }

    public function write($name,$value){
        self::$data[$name] = $value;
    }

    public function read($name){
        if(empty(self::$data[$name])){
            return null;
        }else{
            return self::$data[$name];
        }
    }

    public function readInc($name){
        if(empty($this->inc->$name)){
            return null;
        }else{
            return $this->inc->$name;
        }
    }

    public function delete($name){
        unset(self::$data[$name]);
    }

    function __destruct(){
        $_SESSION['QC_userData'] = self::$data;
    }
}
